# Classes are the backbone to Object Oriented Programming, or OOP. As you get
# comfortable with Python, classes can become an absolutely integral part of
# our programs

class calculator:
    
    def addition(x,y):
        added = x + y
        print(added)
        
    def subtraction(x,y):
        sub = x - y
        print(sub)

    def multiplication(x,y):
        mult = x * y
        print(mult)

    def division(x,y):
        div = x / y
        print(div)


calculator.subtraction(5,8)
calculator.multiplication(3,5)
calculator.division(5,3)
calculator.addition(5,2)


# Hello World Function
def h1
    puts "Hello World!"
end

# Invoke Function
h1

# Hello World with Inline String Interpolation
def h2(name = "World")
   puts "Hello #{name.capitalize}!"
end

# Call Function with Param
h2 "world 2"

# Call Function with Param
h2 ("world 2") 

# Call Function with Param
h2 ("world 2") ;



